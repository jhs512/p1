// ── 유틸 ──────────────────────────────────────────────────────
type Hashes = Record<string, string>;

function hash(text: string): string {
  return createHash("sha256").update(text).digest("hex").slice(0, 16);
}
function loadHashes(): Hashes {
  if (!existsSync(HASH_FILE)) return {};
  return JSON.parse(readFileSync(HASH_FILE, "utf-8")) as Hashes;
}

// ── 정규식 이스케이프 ──────────────────────────────────────────
function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

// ── 인라인 발음 문법: [표시(발음:읽기)] ────────────────────────
// TTS용: [유튜브(발음:유튭)] → 유튭
// pronMap 적용: 전역/강좌 발음 사전 (inline이 최우선 — placeholder로 보호 후 복원)
function toTTSText(text: string, pronMap: Record<string, string> = {}): string {
  // Step 1: inline [X(발음:Y)] → placeholder 보호 (inline 최우선)
  const inlines: string[] = [];
  let t = text.replace(/\[([^(]+)\(발음:([^)]*)\)\]/g, (_m, _d, p) => {
    inlines.push(p.trim());
    return `\x01${inlines.length - 1}\x01`;
  });
  // Step 2: 발음 사전 적용 (긴 키 먼저 — longest-match-first)
  const sortedKeys = Object.keys(pronMap).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    t = t.replace(new RegExp(escapeRegex(key), "g"), pronMap[key]);
  }
  // Step 3: inline placeholder 복원
  t = t.replace(/\x01(\d+)\x01/g, (_m, i) => inlines[parseInt(i)]);
  // Step 4: \n → 공백 (TTS 쉼 방지), 연속 공백 정규화
  t = t.replace(/\n/g, " ").replace(/  +/g, " ");
  return t;
}

// ── 나레이션 토크나이저 ────────────────────────────────────────
// [X(발음:Y)] → { display: X, tts: Y.split(" ") }
// 일반 단어    → { display: word, tts: [word] }
//
// ⚠️ 핵심: [X(발음:Y)]바로뒤 처럼 ] 다음에 공백 없이 이어지는 문자는
//   suffix로 캡처해 display·tts 모두 한 토큰으로 합친다.
//   예) [개수(발음:개쑤)]처럼 → display:"개수처럼", tts:["개쑤처럼"]
//   이렇게 해야 toDisplayText().split(/\s+/) 의 display 토큰 수 =
//   buildGlobalAlignment 의 globalTtsCount 와 일치한다.
type NarrationToken = { display: string; tts: string[] };
function tokenizeNarration(text: string): NarrationToken[] {
  const tokens: NarrationToken[] = [];
  // Group 1,2: inline [X(발음:Y)], Group 3: trailing non-whitespace suffix, Group 4: plain word
  const re = /\[([^(]+)\(발음:([^)]*)\)\](\S*)|(\S+)/g;
  let m;
  while ((m = re.exec(text)) !== null) {
    if (m[1] !== undefined) {
      const pron   = m[2].trim();
      const suffix = m[3] ?? "";
      const displayText = m[1] + suffix;
      if (pron === "") {
        // 묵음: suffix만 남기되, suffix도 없으면 완전 묵음
        tokens.push({ display: displayText, tts: suffix ? [suffix] : [] });
      } else {
        const pronWords = pron.split(/\s+/);
        // suffix는 마지막 TTS 단어에 붙인다
        // (e.g. pron="낫 트루" suffix="는" → ["낫","트루는"])
        if (suffix) pronWords[pronWords.length - 1] += suffix;
        tokens.push({ display: displayText, tts: pronWords });
      }
    } else {
      tokens.push({ display: m[4], tts: [m[4]] });
    }
  }
  return tokens;
}

// ── 전역 TTS 정렬 테이블 ───────────────────────────────────────
// narration 모든 문장의 display 단어 → 전역 TTS 인덱스 매핑
// 반환값:
//   globalTtsCount: 전체 TTS 단어 수
//   displayWords:   각 display 단어의 (sentenceIdx, displayIdx, firstTtsIdx, ttsCount)
type DisplayWordInfo = {
  sentenceIdx: number;
  displayIdx:  number;
  firstTtsIdx: number;   // 이 단어의 TTS 단어들이 시작하는 전역 인덱스
  ttsCount:    number;   // TTS 확장 단어 수 (0 = 발화 없음)
};

export function buildGlobalAlignment(
  narration: string[],
): { globalTtsCount: number; displayWords: DisplayWordInfo[] } {
  const displayWords: DisplayWordInfo[] = [];
  let globalTtsIdx = 0;
  for (let si = 0; si < narration.length; si++) {
    const tokens = tokenizeNarration(narration[si]);
    for (let di = 0; di < tokens.length; di++) {
      const { tts } = tokens[di];
      displayWords.push({ sentenceIdx: si, displayIdx: di, firstTtsIdx: globalTtsIdx, ttsCount: tts.length });
      globalTtsIdx += tts.length;
    }
  }
  return { globalTtsCount: globalTtsIdx, displayWords };
}

// ── AUDIO_CONFIG 파일 쓰기 ────────────────────────────────────
type SceneAudioData = {
  durationInFrames: number;
  narrationSplits: number[];
  sentenceEndFrames: number[];
  speechStartFrame: number;
  speechEndFrame: number;
  wordStartFrames: number[][];  // display 단어별 시작 프레임 (문장별)
  wordEndFrames: number[][];    // display 단어별 종료 프레임 (발화 끝 시점)
  wordStartMs: number[][];      // display 단어별 시작 밀리초 (문장별) — SRT 생성 등에 활용
};

function writeAudioConfig(config: Record<string, SceneAudioData>): void {
  const audioConfigFile = path.join(COMPOSITION_DIR, compositionId + "-audio.ts");
  const lines = Object.entries(config)
    .map(([k, v]) => {
      const wfStr  = (v.wordStartFrames ?? []).map((s) => `[${s.join(",")}]`).join(",");
      const weStr  = (v.wordEndFrames   ?? []).map((s) => `[${s.join(",")}]`).join(",");
      const wmsStr = (v.wordStartMs     ?? []).map((s) => `[${s.join(",")}]`).join(",");
      return `  ${k.padEnd(16)}: { durationInFrames: ${v.durationInFrames}, narrationSplits: [${v.narrationSplits.join(", ")}], sentenceEndFrames: [${v.sentenceEndFrames.join(", ")}], speechStartFrame: ${v.speechStartFrame}, speechEndFrame: ${v.speechEndFrame}, wordStartFrames: [${wfStr}], wordEndFrames: [${weStr}], wordStartMs: [${wmsStr}] },`;
    })
    .join("\n");
  const content = `// AUTO-GENERATED by \`pnpm sync ${compositionId}\` — do not edit manually\n\nexport const AUDIO_CONFIG = {\n${lines}\n} as const;\n`;
  writeFileSync(audioConfigFile, content, "utf-8");
  console.log(`\n📝  ${audioConfigFile} 업데이트 완료`);
}

