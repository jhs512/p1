/**
 * scripts/sync.ts
 *
 * 사용법: pnpm sync <compositionId>
 *   예)  pnpm sync 0001
 *
 * 자동 처리:
 *  1. narration 텍스트 해시로 변경된 씬만 감지
 *  2. edge-tts 로 mp3 재생성
 *  3. ffprobe 로 실측 duration → durationInFrames 계산
 *  4. ffmpeg silencedetect 로 문장 분기 프레임 감지 → narrationSplits 계산
 *  5. 결과를 <compositionId>-audio.ts 파일로 자동 저장 (regex 패치 불필요)
 */

import { buildSync } from "esbuild";
import { createHash } from "crypto";
import { readFileSync, writeFileSync, existsSync, readdirSync } from "fs";
import { spawnSync } from "child_process";
import path from "path";

const FPS = 30;
const SCENE_TAIL_FRAMES = 15;
const PUBLIC_DIR = "public";
const SRC_DIR = "src/compositions";

// ── CLI 인수 ───────────────────────────────────────────────────
const compositionId = process.argv[2];
if (!compositionId) {
  console.error("Usage: pnpm sync <compositionId>   e.g.  pnpm sync 0001");
  process.exit(1);
}

// ── 컴포지션 파일 자동 탐색 ───────────────────────────────────
const match = readdirSync(SRC_DIR).find(
  (f) => f.startsWith(compositionId + "-") && f.endsWith(".tsx")
);
if (!match) {
  console.error(`❌  ${SRC_DIR}/${compositionId}-*.tsx 파일을 찾을 수 없습니다.`);
  process.exit(1);
}
const COMPOSITION_FILE = path.join(SRC_DIR, match);
const HASH_FILE = `.${compositionId}-audio-hashes.json`;
console.log(`📄  ${COMPOSITION_FILE}\n`);

// ── AUDIO_CONFIG 파일이 없으면 스텁 생성 (esbuild 번들 실패 방지) ──
// VIDEO_CONFIG 로드 시 AUDIO_CONFIG 의 각 씬이 undefined 이면 오류 발생하므로
// Proxy 기반 스텁으로 생성. 실제 값은 메인 루프 후 writeAudioConfig 로 덮어씀.
const _audioConfigBootstrap = path.join(SRC_DIR, compositionId + "-audio.ts");
if (!existsSync(_audioConfigBootstrap)) {
  writeFileSync(
    _audioConfigBootstrap,
    `// AUTO-GENERATED by \`pnpm sync ${compositionId}\` — do not edit manually\n\nconst _stub = { durationInFrames: 30, narrationSplits: [] as number[] };\nexport const AUDIO_CONFIG: Record<string, { durationInFrames: number; narrationSplits: number[] }> = new Proxy({}, { get: () => _stub });\n`,
    "utf-8"
  );
  // audio config 파일이 없었다는 것은 공백 상태 → 해시를 초기화하여 모든 씬 재처리
  if (existsSync(HASH_FILE)) {
    writeFileSync(HASH_FILE, "{}", "utf-8");
  }
}

// ── VIDEO_CONFIG 로드 (esbuild 번들 + Node 실행) ──────────────
type SceneEntry = {
  narration?: string[];
  audio: string;
};

function loadConfig(): {
  VOICE: string;
  RATE: string;
  PRONUNCIATION: Record<string, string>;
  VIDEO_CONFIG: Record<string, SceneEntry>;
} {
  const result = buildSync({
    entryPoints: [COMPOSITION_FILE],
    bundle: true,
    platform: "node",
    format: "cjs",
    write: false,
    packages: "external",
    logLevel: "silent",
  });
  const code = result.outputFiles[0].text;
  const mod = { exports: {} as Record<string, unknown> };
  const stub = new Proxy({}, {
    get(_: object, prop: string | symbol) {
      if (prop === "then") return undefined;
      return (...args: unknown[]) => { void args; return stub; };
    },
  });
  const realRequire = require;
  const mockRequire = (id: string): unknown => {
    if (id === "react" || id === "remotion" || id.startsWith("@remotion/")) return stub;
    return realRequire(id);
  };
  new Function("module", "exports", "require", code)(mod, mod.exports, mockRequire);
  return mod.exports as ReturnType<typeof loadConfig>;
}

const { VOICE, RATE, PRONUNCIATION, VIDEO_CONFIG } = loadConfig();

// ── 유틸 ──────────────────────────────────────────────────────
type Hashes = Record<string, string>;

function hash(text: string): string {
  return createHash("sha256").update(text).digest("hex").slice(0, 16);
}

function loadHashes(): Hashes {
  if (!existsSync(HASH_FILE)) return {};
  return JSON.parse(readFileSync(HASH_FILE, "utf-8")) as Hashes;
}

function applyPronunciation(text: string): string {
  let r = text;
  for (const [k, v] of Object.entries(PRONUNCIATION)) r = r.replaceAll(k, v);
  return r;
}

// ── 문장 분기 프레임 감지 ─────────────────────────────────────
function detectSplits(audioFile: string, sentenceCount: number): number[] {
  if (sentenceCount <= 1) return [];

  // 전체 길이
  const probeRes = spawnSync(
    "ffprobe",
    ["-v", "quiet", "-show_entries", "format=duration", "-of", "csv=p=0", audioFile],
    { encoding: "utf-8" }
  );
  const totalSecs = parseFloat(probeRes.stdout.trim());

  // 무음 감지
  const ffRes = spawnSync(
    "ffmpeg",
    ["-i", audioFile, "-af", "silencedetect=n=-25dB:d=0.2", "-f", "null", "-"],
    { encoding: "utf-8" }
  );
  const output = (ffRes.stderr ?? "") + (ffRes.stdout ?? "");

  const entries: { time: number; duration: number }[] = [];
  for (const line of output.split("\n")) {
    const m = line.match(/silence_end:\s*([\d.]+)\s*\|\s*silence_duration:\s*([\d.]+)/);
    if (m) entries.push({ time: parseFloat(m[1]), duration: parseFloat(m[2]) });
  }

  // 문장 사이 무음: 앞뒤 여백 제외, duration > 0.25s 이상인 것만, 필요한 수만큼
  const breaks = entries
    .filter((e) => e.time > 0.4 && e.time < totalSecs - 0.3 && e.duration > 0.25)
    .slice(0, sentenceCount - 1);

  return breaks.map((b) => Math.round(b.time * FPS));
}

// ── AUDIO_CONFIG 파일 쓰기 ────────────────────────────────────
function writeAudioConfig(config: Record<string, { durationInFrames: number; narrationSplits: number[] }>): void {
  const audioConfigFile = path.join(SRC_DIR, compositionId + "-audio.ts");
  const lines = Object.entries(config)
    .map(([k, v]) => `  ${k.padEnd(16)}: { durationInFrames: ${v.durationInFrames}, narrationSplits: [${v.narrationSplits.join(", ")}] },`)
    .join("\n");
  const content = `// AUTO-GENERATED by \`pnpm sync ${compositionId}\` — do not edit manually\n\nexport const AUDIO_CONFIG = {\n${lines}\n} as const;\n`;
  writeFileSync(audioConfigFile, content, "utf-8");
  console.log(`\n📝  ${audioConfigFile} 업데이트 완료`);
}

// ── 메인 루프 ─────────────────────────────────────────────────
const hashes = loadHashes();
let changed = false;

// 기존 AUDIO_CONFIG 로드 (skip된 씬의 값 보존용)
const audioConfigFile = path.join(SRC_DIR, compositionId + "-audio.ts");
let existingAudioConfig: Record<string, { durationInFrames: number; narrationSplits: number[] }> = {};
if (existsSync(audioConfigFile)) {
  try {
    const raw = readFileSync(audioConfigFile, "utf-8");
    // 간단한 eval 대신 개행 파싱으로 값 추출
    for (const line of raw.split("\n")) {
      const m = line.match(/^\s+(\w+)\s*:\s*\{ durationInFrames: (\d+), narrationSplits: \[([^\]]*)\]/);
      if (m) {
        const splits = m[3].trim() ? m[3].split(",").map(s => parseInt(s.trim())) : [];
        existingAudioConfig[m[1]] = { durationInFrames: parseInt(m[2]), narrationSplits: splits };
      }
    }
  } catch { /* ignore */ }
}

const audioConfig: Record<string, { durationInFrames: number; narrationSplits: number[] }> = {};

for (const [key, scene] of Object.entries(VIDEO_CONFIG)) {
  const narration = scene.narration;
  if (!narration) continue;

  const ttsText = applyPronunciation(narration.join(" "));
  const audio = scene.audio;
  const newHash = hash(VOICE + RATE + ttsText);

  if (hashes[key] === newHash) {
    console.log(`[skip] ${audio}`);
    if (existingAudioConfig[key]) audioConfig[key] = existingAudioConfig[key];
    continue;
  }

  console.log(`[gen]  ${audio}`);

  // 1) TTS 생성
  const ttsRes = spawnSync(
    "edge-tts",
    ["--voice", VOICE, "--rate", RATE, "--text", ttsText, "--write-media", `${PUBLIC_DIR}/${audio}`],
    { stdio: "inherit" }
  );
  if (ttsRes.status !== 0) {
    console.error(`❌  Failed: ${audio}`);
    process.exit(1);
  }

  // 2) durationInFrames 계산
  const probeRes = spawnSync(
    "ffprobe",
    ["-v", "quiet", "-show_entries", "format=duration", "-of", "csv=p=0", `${PUBLIC_DIR}/${audio}`],
    { encoding: "utf-8" }
  );
  if (probeRes.status === 0) {
    const secs = probeRes.stdout.trim();
    console.log(`       실측 ${parseFloat(secs).toFixed(2)}s`);
    audioConfig[key] = { durationInFrames: Math.ceil(parseFloat(secs) * FPS) + SCENE_TAIL_FRAMES, narrationSplits: [] };
  }

  // 3) narrationSplits 계산
  const splits = detectSplits(`${PUBLIC_DIR}/${audio}`, narration.length);
  if (splits.length > 0) {
    audioConfig[key].narrationSplits = splits;
    console.log(`       narrationSplits → [${splits.join(", ")}] ✅`);
  }

  hashes[key] = newHash;
  changed = true;
}

// AUDIO_CONFIG 파일 업데이트 (변경된 씬이 하나라도 있으면 쓰기)
if (changed) {
  writeAudioConfig(audioConfig);
}

if (changed) {
  writeFileSync(HASH_FILE, JSON.stringify(hashes, null, 2));
  console.log("\n✅  Done. Hashes updated.");
} else {
  console.log("\n✅  All audio up to date.");
}
